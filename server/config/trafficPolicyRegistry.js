const DEFAULT_ERROR_BEHAVIOR = 'Return a minimized JSON error with requestId, stable code, and no account or policy internals.';
const NO_AUTOMATIC_RETRY = 'No automatic retry for mutations; require user intent or idempotency-aware retry.';
const READ_RETRY = 'Read-only retry may use bounded jittered retry after cache or dependency timeout.';

const toArray = (value) => (Array.isArray(value) ? value : []);

const firstPositive = (...values) => values.find((value) => Number(value) > 0) || 0;

const limitSummary = (budget, userType) => {
    if (userType === 'admin') {
        return {
            perIp: budget.perIp,
            perUser: firstPositive(budget.perAccount, budget.perSession),
            windowSeconds: budget.windowSeconds,
        };
    }
    if (userType === 'authenticated') {
        return {
            perIp: budget.perIp,
            perUser: budget.perAccount,
            perSession: budget.perSession,
            windowSeconds: budget.windowSeconds,
        };
    }
    return {
        perIp: budget.perIp,
        perSession: budget.perSession,
        windowSeconds: budget.windowSeconds,
    };
};

const patternList = (definition) => [
    ...toArray(definition.exactPaths),
    ...toArray(definition.pathPrefixes).map((entry) => `${entry}*`),
    ...toArray(definition.pathIncludes).map((entry) => `*${entry}*`),
    ...(definition.staticAssets ? ['static-extension-assets'] : []),
];

const definePolicy = (definition) => {
    const budget = getTrafficBudget(definition.routeClass);
    const mutating = definition.methodGroup === 'mutation';
    const policy = {
        id: definition.id,
        componentName: definition.componentName,
        profile: definition.profile,
        profileLabel: PROFILE_LABELS[definition.profile] || definition.profile,
        routeClass: definition.routeClass,
        riskLevel: definition.riskLevel || budget.costRisk || 'medium',
        costClass: definition.costClass || budget.costRisk || 'medium',
        routePatterns: patternList(definition),
        exactPaths: toArray(definition.exactPaths).map(normalizeRoutePath),
        pathPrefixes: toArray(definition.pathPrefixes).map(normalizeRoutePath),
        pathIncludes: toArray(definition.pathIncludes),
        methods: toArray(definition.methods).map(normalizeMethod),
        methodGroup: definition.methodGroup || 'any',
        staticAssets: Boolean(definition.staticAssets),
        userIntent: definition.userIntent,
        abuseRisk: definition.abuseRisk,
        normalTrafficPattern: definition.normalTrafficPattern,
        burstAllowance: budget.burst,
        hardCap: budget.sustained,
        anonymousLimit: limitSummary(budget, 'anonymous'),
        authenticatedLimit: limitSummary(budget, 'authenticated'),
        adminLimit: limitSummary(budget, 'admin'),
        perIpLimit: budget.perIp,
        perUserLimit: budget.perAccount,
        perAccountLimit: budget.perAccount,
        perDeviceSessionLimit: budget.perSession,
        burstWindow: { seconds: budget.windowSeconds, max: budget.burst },
        sustainedWindow: { seconds: budget.windowSeconds, max: budget.sustained },
        bodySizeBytes: budget.maxBodyBytes,
        timeoutMs: budget.timeoutMs,
        dependencyTimeoutMs: definition.dependencyTimeoutMs || Math.max(1000, Math.floor(Number(budget.timeoutMs || 0) * 0.8)),
        retryPolicy: definition.retryPolicy || (mutating ? NO_AUTOMATIC_RETRY : READ_RETRY),
        cacheRule: definition.cacheRule || budget.cacheMode || 'no-store',
        overloadBehavior: definition.overloadBehavior || (budget.canDegrade
            ? 'May shed with 503 during overload while preserving core routes.'
            : 'Do not shed automatically; fail closed or rely on route-specific guard.'),
        publicErrorBehavior: definition.publicErrorBehavior || DEFAULT_ERROR_BEHAVIOR,
        failMode: definition.failMode || budget.productionFailMode,
        observabilityTags: Object.freeze({
            component: definition.id,
            profile: definition.profile,
            routeClass: definition.routeClass,
            metric: 'aura_traffic_budget_denied_total',
        }),
        authRequired: Boolean(definition.authRequired),
        adminRequired: Boolean(definition.adminRequired),
        signatureRequired: Boolean(definition.signatureRequired),
        flowProtectionRequired: Boolean(definition.flowProtectionRequired),
        idempotencyRequired: Boolean(definition.idempotencyRequired),
        idempotencyDecision: definition.idempotencyDecision || '',
        bodyAccepted: definition.bodyAccepted !== undefined ? Boolean(definition.bodyAccepted) : mutating,
        fileValidationRequired: Boolean(definition.fileValidationRequired),
        quotaRequired: Boolean(definition.quotaRequired),
        concurrencyCapRequired: Boolean(definition.concurrencyCapRequired),
        concurrencyCap: definition.concurrencyCap || '',
        routeLevelLimiterEvidence: definition.routeLevelLimiterEvidence || '',
        guardEvidence: definition.guardEvidence || '',
        cacheEvidence: definition.cacheEvidence || 'server/middleware/cachePolicy.js',
        timeoutEvidence: definition.timeoutEvidence || 'server/middleware/requestTimeouts.js',
        testCoverage: toArray(definition.testCoverage),
        ownerNote: definition.ownerNote || 'Runtime owner: server traffic fortress.',
    };

    return Object.freeze(policy);
};

