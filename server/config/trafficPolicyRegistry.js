const TRAFFIC_POLICY_REGISTRY = Object.freeze(POLICY_DEFINITIONS.map(definePolicy));

const methodMatches = (policy, method) => {
    const normalizedMethod = normalizeMethod(method);
    if (policy.methods.length) return policy.methods.includes(normalizedMethod);
    if (policy.methodGroup === 'mutation') return STATE_CHANGING_METHODS.has(normalizedMethod);
    if (policy.methodGroup === 'read') return !STATE_CHANGING_METHODS.has(normalizedMethod);
    return true;
};

const pathMatches = (policy, routePath) => {
    if (policy.staticAssets && hasStaticExtension(routePath)) return true;
    if (policy.exactPaths.includes(routePath)) return true;
    if (policy.pathPrefixes.some((prefix) => routePath === prefix || routePath.startsWith(`${prefix}/`) || routePath.startsWith(prefix))) return true;
    return policy.pathIncludes.some((needle) => routePath.includes(needle));
};

const getTrafficPolicyForRoute = ({ method = 'GET', path = '/', originalUrl = '' } = {}) => {
    const routePath = normalizeRoutePath(path || originalUrl);
    return TRAFFIC_POLICY_REGISTRY.find((policy) => methodMatches(policy, method) && pathMatches(policy, routePath))
        || TRAFFIC_POLICY_REGISTRY[TRAFFIC_POLICY_REGISTRY.length - 1];
};

const classifyRouteFromRegistry = (route = {}) => getTrafficPolicyForRoute(route).routeClass;

const getTrafficPolicyById = (id) => TRAFFIC_POLICY_REGISTRY.find((policy) => policy.id === id) || null;

const listTrafficPolicies = () => TRAFFIC_POLICY_REGISTRY.slice();

const listTrafficComponents = () => Array.from(new Set(TRAFFIC_POLICY_REGISTRY.map((policy) => policy.componentName)));

const buildTrafficPolicyMatrix = () => TRAFFIC_POLICY_REGISTRY.map((policy) => ({
    component: policy.componentName,
    profile: policy.profileLabel,
    routeExamples: policy.routePatterns.join(', '),
    userType: policy.adminRequired ? 'admin' : (policy.authRequired ? 'authenticated' : 'anonymous or mixed'),
    abuseRisk: policy.abuseRisk,
    rateLimit: `ip ${policy.perIpLimit}/${policy.sustainedWindow.seconds}s; user ${policy.perUserLimit || 0}/${policy.sustainedWindow.seconds}s; session ${policy.perDeviceSessionLimit || 0}/${policy.sustainedWindow.seconds}s`,
    burstLimit: `${policy.burstWindow.max}/${policy.burstWindow.seconds}s`,
    sustainedLimit: `${policy.sustainedWindow.max}/${policy.sustainedWindow.seconds}s`,
    bodySizeBudget: policy.bodySizeBytes,
    timeoutBudget: policy.timeoutMs,
    retryPolicy: policy.retryPolicy,
    cachePolicy: policy.cacheRule,
    overloadResponse: policy.overloadBehavior,
    failMode: policy.failMode,
    tests: policy.testCoverage.join(', '),
    ownerNote: policy.ownerNote,
}));

